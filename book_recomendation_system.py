# STEP 6 ; COLLABORATIVE FILTERING WITH K-NEAREST NEIGHBOURS )KNN()
#  6a. Build User-Item pivot table
# Rows = book titles, Columns = user IDs, Values = rating
pivot = df.pivot_table(index='Title', columns='UserID', values='Rating')
pivot.fillna(0, inplace=True)

print(f'Pivot table shape: {pivot.shape}  (books × users)')

#6b. Convert to sparse matrix (memory-efficient)
# Most ratings are 0, so sparse format only stores non-zero values
sparse_matrix = csr_matrix(pivot.values)

print(f'Sparse matrix shape : {sparse_matrix.shape}')
print(f'Non-zero elements   : {sparse_matrix.nnz:,}')
print(f'Sparsity            : {1 - sparse_matrix.nnz / (sparse_matrix.shape[0] * sparse_matrix.shape[1]):.2%}')

#  6c. Fit the KNN model
# algorithm='brute' computes exact distances; metric='cosine' measures direction similarity
knn_model = NearestNeighbors(metric='cosine', algorithm='brute', n_neighbors=10, n_jobs=-1)
knn_model.fit(sparse_matrix)

print('✅ KNN model fitted!')

#  6d. Recommendation function
def get_recommendations(book_title, n_recommendations=5):
    """
    Given a book title, return the n most similar books using KNN.

    Parameters:
        book_title (str): Exact or partial book title
        n_recommendations (int): Number of books to recommend

    Returns:
        DataFrame with recommended books and similarity scores
    """
    # Fuzzy-match: find the closest title in our index
    matches = [t for t in pivot.index if book_title.lower() in t.lower()]
    if not matches:
        print(f"❌ No book found matching '{book_title}'. Try a different title.")
        return None

    matched_title = matches[0]
    print(f"🔍 Finding books similar to: '{matched_title}'\n")

    # Get the book's row index in the pivot table
    book_idx = pivot.index.get_loc(matched_title)

    # Query the KNN model (returns distances and indices)
    distances, indices = knn_model.kneighbors(
        sparse_matrix[book_idx],
        n_neighbors=n_recommendations + 1  # +1 because the book itself is returned
    )

    # Build results dataframe (skip index 0 = the book itself)
    results = []
    for i, idx in enumerate(indices.flatten()[1:]):
        rec_title = pivot.index[idx]
        similarity = 1 - distances.flatten()[i + 1]  # cosine similarity = 1 - cosine distance
        author = df[df['Title'] == rec_title]['Author'].iloc[0] if rec_title in df['Title'].values else 'N/A'
        results.append({'Rank': i+1, 'Title': rec_title, 'Author': author, 'Similarity': round(similarity, 4)})

    return pd.DataFrame(results)


