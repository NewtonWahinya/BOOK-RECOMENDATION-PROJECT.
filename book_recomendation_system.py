#  7b. Elbow Method — find optimal number of clusters
# We compute inertia (sum of squared distances to cluster centres) for K=2..15
inertias = []
K_range = range(2, 16)

for k in K_range:
    km = KMeans(n_clusters=k, random_state=42, n_init=10)
    km.fit(book_vectors_50)
    inertias.append(km.inertia_)
    print(f'  K={k}  inertia={km.inertia_:.0f}')

plt.figure(figsize=(10, 4))
